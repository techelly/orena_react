/**
 * New Number Properties
 * ES6 added the following properties to the Number object
 * - EPSILON
 * - MIN_SAFE_INTEGER
 * - MAX_SAFE_INTEGER
 */
/**
 * The word Epsilon is the Greek letter and it stands (which means) 
 * an arbitrary number (smallest possible numbers) and we use it in 
 * the algebraic inequality's concepts. We use Epsilon to denote the 
 * smallest quantity like a term which is taken as the zero in some limit.
 * 
 */
let x = Number.EPSILON;
console.log(x);
let y = Number.MIN_SAFE_INTEGER;
console.log(y);
let z = Number.MAX_SAFE_INTEGER;
console.log(z);