/**
 * String.includes() method returns true if a string contains a specified value, otherwise
 * false
 * 
 */

let greet = "Hello Everyone, Welcome to JS class";
console.log(greet.includes("to"));
console.log(greet.includes("how"));
console.log("******************************************************")
/**
 * String.startsWith() returns true if a string begins with a specified value, otherwise false
 */

console.log(greet.startsWith("Hello"));
console.log(greet.startsWith("Hi"));

console.log("******************************************************")
/**
 * String.endsWith method returns true if a string ends with a specified value,otherwise false
 * 
 */

console.log(greet.endsWith("class"));
console.log(greet.endsWith("!"));

